import React from 'react';

/**
 * Компонент CommentList призначений для відображення списку коментарів до статті.
 * Він приймає масив коментарів як пропс та рендерить кожен коментар у відповідному форматі.
 *
 * @param {object} props - Пропси компонента.
 * @param {Array<object>|undefined} props.comments - Масив об'єктів коментарів. Кожен об'єкт повинен мати властивості `name` (опціонально) та `text`. Може бути `undefined` або `null`.
 */
function CommentList({ comments }) {
  // Перевірка: якщо масив `comments` не існує або порожній,
  // виводимо повідомлення про відсутність коментарів.
  if (!comments || comments.length === 0) {
    return <p>Коментарів ще немає.</p>;
  }

  // Якщо коментарі є, рендеримо список
  return (
    <div className="comments-list"> {/* Контейнер для всього списку */}
      {/* Використовуємо метод `map` для ітерації по масиву коментарів */}
      {comments.map((comment, index) => (
        // Для кожного коментаря створюємо окремий блок `div`
        // ВАЖЛИВО: Використання `index` як ключа (`key`) не є найкращою практикою,
        // особливо якщо список може змінюватися (додавання/видалення елементів).
        // Краще використовувати унікальний ID коментаря, якщо він доступний (`comment.id`).
        <div className="comment-item" key={index}> {/* Використовуємо index як тимчасовий ключ */}
          {/* Відображаємо ім'я автора. Якщо ім'я відсутнє, виводимо "Анонім". */}
          <p className="comment-author">{comment.name || 'Анонім'}</p>
          {/* Відображаємо текст коментаря */}
          <p className="comment-text">{comment.text}</p>
        </div>
      ))}
    </div>
  );
}

// Експортуємо компонент для використання в інших частинах додатку
export default CommentList;